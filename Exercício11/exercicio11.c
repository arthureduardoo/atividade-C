/*
 * Respostas sobre Pilha e Fila:
 * 
 * a) Diferença entre pilha e fila:
 * - Pilha: Segue o princípio LIFO (último a entrar, primeiro a sair).
 *   Exemplo: Uma pilha de pratos, onde você pega o prato no topo.
 * - Fila: Segue o princípio FIFO (primeiro a entrar, primeiro a sair).
 *   Exemplo: Uma fila de pessoas no caixa, onde o primeiro da fila é atendido primeiro.
 * 
 * b) Vantagens e desvantagens da fila:
 * - Vantagens: Ideal para processar dados em ordem, como em filas de impressão ou sistemas de atendimento.
 * - Desvantagens: Inserções e remoções só acontecem nas extremidades (início/fim), o que pode limitar a flexibilidade.
 * 
 * c) Vantagens e desvantagens da pilha:
 * - Vantagens: Simples e eficiente para tarefas como desfazer ações ou controle de chamadas de funções.
 * - Desvantagens: Só permite acessar o elemento do topo, dificultando o acesso direto aos outros elementos.
 * 
 * d) Aplicações:
 * - Fila: Usada em sistemas de agendamento, filas de impressão e processamento de mensagens.
 * - Pilha: Usada para execução de funções, desfazer ações (como CTRL+Z) e algoritmos de navegação.
 * 
 * e) Diferença entre pilhas/filas e listas:
 * - Pilhas e filas: Acesso controlado (somente topo ou início/fim).
 * - Lista: Elementos podem ser acessados em qualquer posição, o que oferece mais flexibilidade.
 */
